unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	int			i;
	int			sz;

	i = 0;
	sz = (int)size;
	while (i < sz - 1 && src[i] != 0)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	while (src[i] != '\0')
	{
		i++;
	}
	return (i);
}
